
public class cardAtm {
	private String id;
	private String maPin;
	public cardAtm(String id, String maPin) {
		this.id = id;
		this.maPin = maPin;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMaPin() {
		return maPin;
	}
	public void setMaPin(String maPin) {
		this.maPin = maPin;
	}
	
	
}
